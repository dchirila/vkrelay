// Pre-compiled SPIR-V for the Vulkan 1.3 support canary / real-backend tests: a compute kernel
// whose ONLY uniform input is an INLINE_UNIFORM_BLOCK descriptor (binding 0, 64 bytes = 16 u32s,
// declared as a std140 uvec4[4] so the bytes are tight) writing `out[i] = vals[wg % 16] + i` to
// an SSBO (binding 1). The IUB index is WORKGROUP-UNIFORM by design -- 16 workgroups cover all
// 16 words, so every byte of the inline data is proven delivered -- because that is the access
// pattern inline uniform blocks exist for (per-draw/dispatch constants), and because DIVERGENT
// intra-block indexing of IUB data is miscompiled by the AMD Windows driver (native no-relay
// repro on a Radeon 610M, driver 1.3.280: every lane reads the block as if the divergent index
// were lane 0's -- the IUB lowering scalarizes the load; NVIDIA is byte-exact on the identical
// SPIR-V). The relay's proof obligation is faithful DELIVERY of the bytes, not the host
// compiler's divergent-index correctness. The SPIR-V of a uniform block is IDENTICAL whether
// the descriptor is a classic UNIFORM_BUFFER or an INLINE_UNIFORM_BLOCK -- the type is a
// descriptor-layout distinction -- so this kernel proves the IUB write path end to end.
//
// Checked in (not built) so the Windows real-backend test needs no glslang build dependency --
