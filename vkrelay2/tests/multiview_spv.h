// Pre-compiled SPIR-V for the multiview serve-proof canary. A full-screen triangle whose fragment
// color is chosen strictly by gl_ViewIndex -- view 0 =
// red, view 1 = green. The canary renders it once through a viewMask render pass (RP2) and once
// through a viewMask dynamic-rendering scope, then reads back each array layer: layer 0 must be
// all-red, layer 1 all-green. If multiview did not actually run per-view on the host both layers
// would be identical, so this is the end-to-end proof behind flipping kRelayServesMultiview.
//
// Checked in (not built) so the canary needs no glslang build dependency. Regenerate with:
//   glslangValidator -V multiview.vert --vn kMultiviewVert -o vert.h
//   glslangValidator -V multiview.frag --vn kMultiviewFrag -o frag.h
//
// multiview.vert (bufferless full-screen triangle):
//   #version 450
//   void main() {
//     vec2 p[3] = vec2[](vec2(-1.0,-1.0), vec2(3.0,-1.0), vec2(-1.0,3.0));
//     gl_Position = vec4(p[gl_VertexIndex], 0.0, 1.0);
//   }
// multiview.frag (color by view index -- THE proof):
//   #version 450
//   #extension GL_EXT_multiview : require
//   layout(location = 0) out vec4 o;
//   void main() { o = (gl_ViewIndex == 0) ? vec4(1,0,0,1) : vec4(0,1,0,1); }
